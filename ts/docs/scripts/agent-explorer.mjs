import { promises as fs } from "node:fs";
import path from "node:path";

export const AGENT_EXPLORER_FILE = "explorer.md";

export async function buildAgentExplorer(pkgDirs, rootDir) {
    const agents = [];
    for (const dir of pkgDirs) {
        const agent = await readAgentInfo(dir, rootDir);
        if (agent) agents.push(agent);
    }
    agents.sort((a, b) => a.slug.localeCompare(b.slug));
    return {
        agents,
        markdown: renderAgentExplorerPage(agents),
    };
}

async function readAgentInfo(dir, rootDir) {
    const packageJson = await readJson(path.join(dir, "package.json"));
    if (!packageJson || typeof packageJson.name !== "string") return null;
    const relDir = toPosix(path.relative(rootDir, dir));
    const slug = path.basename(dir);
    const manifestPath = await findManifestPath(path.join(dir, "src"));
    const manifest = manifestPath ? await readJson(manifestPath) : null;
    const schemaPath = await resolveSchemaPath(dir, manifest, manifestPath);
    const actions =
        schemaPath !== null
            ? extractActionsFromSource(
                  await fs.readFile(schemaPath, "utf8").catch(() => ""),
              )
            : [];
    return {
        name: packageJson.name,
        slug,
        relDir,
        emoji:
            typeof manifest?.emojiChar === "string" && manifest.emojiChar.trim()
                ? manifest.emojiChar.trim()
                : "🤖",
        description: firstNonEmpty(
            typeof manifest?.description === "string"
                ? manifest.description
                : "",
            typeof packageJson.description === "string"
                ? packageJson.description
                : "",
        ),
        overviewHref: `./${slug}/overview.md`,
        generatedHref: `./${slug}/generated.md`,
        sourceHref: githubSourceHref(relDir),
        actions,
    };
}

async function resolveSchemaPath(dir, manifest, manifestPath) {
    const originalSchemaFile = manifest?.schema?.originalSchemaFile;
    if (
        manifestPath &&
        typeof originalSchemaFile === "string" &&
        originalSchemaFile.length > 0
    ) {
        const resolved = path.resolve(
            path.dirname(manifestPath),
            originalSchemaFile,
        );
        if (await exists(resolved)) return resolved;
    }
    const srcDir = path.join(dir, "src");
    const schemaCandidates = await findFiles(srcDir, (name) =>
        /Schema\.(?:ts|mts)$/u.test(name),
    );
    let best = null;
    for (const candidate of schemaCandidates.sort()) {
        const source = await fs.readFile(candidate, "utf8").catch(() => "");
        const actions = extractActionsFromSource(source);
        if (actions.length === 0) continue;
        if (best === null || actions.length > best.count) {
            best = { path: candidate, count: actions.length };
        }
    }
    return best?.path ?? null;
}

async function findManifestPath(srcDir) {
    const manifests = await findFiles(
        srcDir,
        (name) => name === "manifest.json" || name.endsWith("Manifest.json"),
    );
    let fallback = null;
    for (const file of manifests.sort()) {
        fallback ??= file;
        const json = await readJson(file);
        if (json?.schema && typeof json.schema === "object") return file;
    }
    return fallback;
}

async function findFiles(dir, predicate) {
    let entries = [];
    try {
        entries = await fs.readdir(dir, { withFileTypes: true });
    } catch {
        return [];
    }
    const files = [];
    for (const entry of entries) {
        const full = path.join(dir, entry.name);
        if (entry.isDirectory()) {
            files.push(...(await findFiles(full, predicate)));
        } else if (entry.isFile() && predicate(entry.name, full)) {
            files.push(full);
        }
    }
    return files;
}

async function readJson(file) {
    try {
        return JSON.parse(await fs.readFile(file, "utf8"));
    } catch {
        return null;
    }
}

async function exists(file) {
    try {
        await fs.access(file);
        return true;
    } catch {
        return false;
    }
}

function githubSourceHref(relDir) {
    return `https://github.com/microsoft/TypeAgent/tree/main/ts/${relDir}`;
}

function firstNonEmpty(...values) {
    for (const value of values) {
        if (typeof value === "string" && value.trim().length > 0) {
            return value.trim();
        }
    }
    return "";
}

function toPosix(value) {
    return value.split(path.sep).join("/");
}

export function renderAgentExplorerPage(agents) {
    const actionCount = agents.reduce(
        (sum, agent) => sum + agent.actions.length,
        0,
    );
    const lines = [
        "# Agent & action explorer",
        "",
        "> Auto-generated by `node ts/docs/scripts/build-wiki.mjs`. Re-run that script whenever agent docs are regenerated so this browser stays in sync.",
        "",
        `Browse **${agents.length} agents** and **${actionCount} typed actions**. Open an agent to see its action surface, then open an action to inspect its parameters and sample phrases.`,
        "",
    ];
    if (agents.length === 0) {
        lines.push("_No agents found._", "");
        return lines.join("\n");
    }
    for (const agent of agents) {
        lines.push(renderAgent(agent));
    }
    return lines.join("\n").trimEnd() + "\n";
}

function renderAgent(agent) {
    const actionLabel =
        agent.actions.length === 1
            ? "1 action"
            : `${agent.actions.length} actions`;
    const lines = [
        `<details class="typeagent-agent-explorer">`,
        `<summary><strong>${escapeHtml(agent.emoji)} ${escapeHtml(agent.slug)}</strong> — ${escapeHtml(agent.description || "No description.")} <em>(${actionLabel})</em></summary>`,
        "",
        `<p><a href="${escapeAttribute(agent.overviewHref)}">Overview</a> · <a href="${escapeAttribute(agent.generatedHref)}">Generated README</a> · <a href="${escapeAttribute(agent.sourceHref)}">Source</a></p>`,
        "",
    ];
    if (agent.actions.length === 0) {
        lines.push(
            "<p><em>No typed actions were detected for this agent.</em></p>",
        );
    } else {
        for (const action of agent.actions) {
            lines.push(renderAction(action));
        }
    }
    lines.push("</details>", "");
    return lines.join("\n");
}

function renderAction(action) {
    const lines = [
        `<details class="typeagent-agent-action">`,
        `<summary><code>${escapeHtml(action.actionName)}</code>${action.description ? ` — ${escapeHtml(action.description)}` : ""}</summary>`,
        "",
        `<p><strong>Type:</strong> <code>${escapeHtml(action.typeName)}</code></p>`,
    ];
    if (action.parameters.length === 0) {
        lines.push("<p><strong>Parameters:</strong> none</p>");
    } else {
        lines.push(
            "<p><strong>Parameters</strong></p>",
            "<table>",
            "<thead><tr><th>Name</th><th>Type</th><th>Required</th><th>Description</th></tr></thead>",
            "<tbody>",
        );
        for (const parameter of action.parameters) {
            lines.push(
                `<tr><td><code>${escapeHtml(parameter.name)}</code></td><td><code>${escapeHtml(parameter.type)}</code></td><td>${parameter.optional ? "optional" : "required"}</td><td>${escapeHtml(parameter.description || "—")}</td></tr>`,
            );
        }
        lines.push("</tbody>", "</table>");
    }
    if (action.samplePhrases.length === 0) {
        lines.push("<p><strong>Sample phrases:</strong> none recorded</p>");
    } else {
        lines.push("<p><strong>Sample phrases</strong></p>", "<ul>");
        for (const phrase of action.samplePhrases) {
            lines.push(`<li><code>${escapeHtml(phrase)}</code></li>`);
        }
        lines.push("</ul>");
    }
    lines.push("</details>", "");
    return lines.join("\n");
}

function escapeHtml(value) {
    return String(value)
        .replace(/&/gu, "&amp;")
        .replace(/</gu, "&lt;")
        .replace(/>/gu, "&gt;")
        .replace(/"/gu, "&quot;");
}

function escapeAttribute(value) {
    return escapeHtml(value).replace(/'/gu, "&#39;");
}

export function extractActionsFromSource(source) {
    const lines = source.split(/\r?\n/u);
    const actions = [];
    for (let i = 0; i < lines.length; i++) {
        const declMatch = /^export\s+type\s+(\w+)\s*=\s*\{/u.exec(lines[i]);
        if (!declMatch) continue;
        const typeName = declMatch[1];
        const leadingComments = [];
        for (let j = i - 1; j >= 0; j--) {
            const trimmed = lines[j].trim();
            if (trimmed.startsWith("//")) {
                leadingComments.unshift(stripCommentMarker(trimmed));
            } else {
                break;
            }
        }
        const { description, samplePhrases } =
            splitDescriptionAndPhrases(leadingComments);
        const bodyEnd = findMatchingBrace(lines, i);
        const bodyLines = lines.slice(i + 1, bodyEnd);
        const actionName = findActionNameLiteral(bodyLines);
        if (actionName === null) continue;
        actions.push({
            typeName,
            actionName,
            description,
            samplePhrases,
            parameters: extractParameters(bodyLines),
        });
    }
    return actions;
}

function stripCommentMarker(line) {
    return line.startsWith("// ") ? line.slice(3) : line.slice(2);
}

function splitDescriptionAndPhrases(comments) {
    const description = [];
    const samplePhrases = [];
    let inPhrases = false;
    for (const comment of comments) {
        const normalized = comment.trim().toLowerCase();
        if (
            normalized === "sample phrase" ||
            normalized === "sample phrase:" ||
            normalized === "sample phrases" ||
            normalized === "sample phrases:"
        ) {
            inPhrases = true;
            continue;
        }
        if (!inPhrases) {
            description.push(comment);
            continue;
        }
        const doubleQuote = /^\s*-\s*"([^"\n]*)"\s*$/u.exec(comment);
        if (doubleQuote) {
            samplePhrases.push(doubleQuote[1]);
            continue;
        }
        const singleQuote = /^\s*-\s*'([^'\n]*)'\s*$/u.exec(comment);
        if (singleQuote) samplePhrases.push(singleQuote[1]);
    }
    return {
        description: description.join(" ").replace(/\s+/gu, " ").trim(),
        samplePhrases,
    };
}

function findMatchingBrace(lines, startLine) {
    let depth = 0;
    for (let i = startLine; i < lines.length; i++) {
        for (const ch of lines[i]) {
            if (ch === "{") depth++;
            else if (ch === "}") {
                depth--;
                if (depth === 0) return i;
            }
        }
    }
    return lines.length;
}

function findActionNameLiteral(bodyLines) {
    for (const line of bodyLines) {
        const match =
            /^\s*actionName\s*:\s*"([^"]+)"\s*;?/u.exec(line) ??
            /^\s*actionName\s*:\s*'([^']+)'\s*;?/u.exec(line);
        if (match) return match[1];
    }
    return null;
}

function extractParameters(bodyLines) {
    let paramOpenLine = -1;
    for (let i = 0; i < bodyLines.length; i++) {
        if (/^\s*parameters\s*\??\s*:\s*\{/u.test(bodyLines[i])) {
            paramOpenLine = i;
            break;
        }
    }
    if (paramOpenLine === -1) return [];
    let depth = 0;
    let paramCloseLine = -1;
    for (let i = paramOpenLine; i < bodyLines.length; i++) {
        for (const ch of bodyLines[i]) {
            if (ch === "{") depth++;
            else if (ch === "}") {
                depth--;
                if (depth === 0) {
                    paramCloseLine = i;
                    break;
                }
            }
        }
        if (paramCloseLine !== -1) break;
    }
    if (paramCloseLine === -1) return [];
    const inner = bodyLines.slice(paramOpenLine + 1, paramCloseLine);
    const params = [];
    let pendingComments = [];
    let nestedDepth = 0;
    let buffer = "";
    const flushField = (raw) => {
        const fieldMatch =
            /^\s*([A-Za-z_$][\w$]*)\s*(\??)\s*:\s*([\s\S]+?)\s*$/u.exec(raw);
        if (!fieldMatch) {
            pendingComments = [];
            return;
        }
        params.push({
            name: fieldMatch[1],
            optional: fieldMatch[2] === "?",
            type: fieldMatch[3].replace(/\s+/gu, " ").trim(),
            description: pendingComments.join(" ").replace(/\s+/gu, " ").trim(),
        });
        pendingComments = [];
    };
    for (const rawLine of inner) {
        const trimmed = rawLine.trim();
        if (trimmed.startsWith("//")) {
            pendingComments.push(stripCommentMarker(trimmed));
            continue;
        }
        if (trimmed.length === 0) continue;
        for (const ch of trimmed) {
            if (ch === "{") nestedDepth++;
            else if (ch === "}") nestedDepth--;
        }
        buffer = buffer.length > 0 ? `${buffer} ${trimmed}` : trimmed;
        if (
            nestedDepth === 0 &&
            (buffer.endsWith(";") || buffer.endsWith(","))
        ) {
            flushField(buffer.replace(/[,;]\s*$/u, ""));
            buffer = "";
        }
    }
    if (buffer.trim().length > 0 && nestedDepth === 0) flushField(buffer);
    return params;
}
